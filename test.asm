EARL:
	SSP 5
	EXT
BTN:
	STO R0 5
	STO R1 15
	STO R2 30
	GDL R0 R0 R0 R1 5       ; R0 R1 => R0 R2 LEADING
	GDL R0 R0 R2 R0 5       ; R0 R1 => R0 R3 TOP
	GDL R2 R0 R2 R1 5       ; R3 R1 => R3 R2 TRAILING
	GDL R0 R1 R2 R1 5       ; R0 R2 => R3 R2 BOTTOM
	STO R0 7
	STO R1 8
	GDC R1 R0 69 7
	ADD R1 6
	GDC R1 R0 120 7
	ADD R1 6
	GDC R1 R0 105 7
	ADD R1 6
	GDC R1 R0 116 7
	JMP CURSOR
CURSOR_COLOR:
	IFQ 3 R12 2 2
	RAN R20 1 155
	IFQ 3 R12 1 3
	GCS
	JMP BTN
	JMP CURSOR
CURSOR:
	MUS R10 R11 R12        ; Get Mouse Position
	GPX R10 R11 R20        ; Drawing Cursor
	JMP CURSOR_COLOR
MAIN:
	JMP BTN