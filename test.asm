EARL:
	SSP 5
	EXT
BTN:
	STO R0 5
	STO R1 5
	STO R2 14
	STO R3 40
	GDL R0 R1 R0 R2 7       ; R0 R1 => R0 R2 LEADING
	GDL R0 R1 R3 R1 7       ; R0 R1 => R0 R3 TOP
	GDL R3 R1 R3 R2 7       ; R3 R1 => R3 R2 TRAILING
	GDL R0 R2 R3 R2 7       ; R0 R2 => R3 R2 BOTTOM
	STO R10 1
	JMP MAIN
MAIN:
	IFQ 3 R10 1 BTN
	JMP EARL